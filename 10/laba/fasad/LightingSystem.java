public class LightingSystem {
    public void turnOn() {
        System.out.println("Свет включен.");
    }

    public void setBrightness(int level) {
        System.out.println("Яркость подсветки установлена на " + level + ".");
    }

    public void turnOff() {
        System.out.println("Свет выключен.");
    }
}
