public class AudioSystem {
    public void turnOn() {
        System.out.println("Аудиосистема включена.");
    }

    public void setVolume(int level) {
        System.out.println("Громкость звука установлена на " + level + ".");
    }

    public void turnOff() {
        System.out.println("Аудиосистема выключена.");
    }
}
